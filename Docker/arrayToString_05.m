function result = arrayToString_05(str1, str2, str3)
    % Diese Funktion nimmt drei Strings als Eingabe und gibt einen kombinierten String zurück
    %
    % Eingabe:
    %   str1 - Erster String
    %   str2 - Zweiter String
    %   str3 - Dritter String
    %
    % Ausgabe:
    %   result - Zusammengesetzter String
    %
    
    % Strings zusammenfügen mit Leerzeichen dazwischen
    result = [str1, ' ', str2, ' ', str3];
end
